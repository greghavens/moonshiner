#!/usr/bin/env bash
# Acceptance harness for lifegame.sh.
# Run from the workspace root:  bash test_lifegame.sh
#
# lifegame.sh targets the kiosk boxes, where it runs with an EMPTY PATH:
# every invocation below launches it as  PATH='' "$BASH" ./lifegame.sh ...
# so any helper that forks out to an external tool (awk, grep, sort, ...)
# fails loudly here. Pure bash only.
set -u
LC_ALL=C
export LC_ALL
unset CDPATH

# run from the harness's own directory even if invoked from elsewhere
[[ $0 == */* ]] && cd -- "${0%/*}"

if [[ ! -f ./lifegame.sh ]]; then
  printf 'FAIL lifegame.sh not found in the workspace root\n'
  exit 1
fi

checks=0
fails=0

assert_eq() { # assert_eq <label> <expected> <actual>
  checks=$((checks + 1))
  if [[ "$2" == "$3" ]]; then
    return 0
  fi
  fails=$((fails + 1))
  printf 'FAIL %s\n--- expected ---\n%s\n--- actual ---\n%s\n---------------\n' "$1" "$2" "$3"
}

run() { PATH='' "$BASH" ./lifegame.sh "$@"; }

