.....
EOF

cat > fixtures/nothing.txt <<'EOF'
! comments only

! no grid rows at all
EOF

# ---- happy-path frame transcripts -------------------------------------------

out=$(run fixtures/blinker.txt 0); st=$?
assert_eq 'blinker gen0 exit status' 0 "$st"
expected=$(cat <<'EOF'
