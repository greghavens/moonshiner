# ---- error handling ----------------------------------------------------------

err_case() { # err_case <label> <expected-stderr> [arg]...
  local label=$1 expected=$2
  shift 2
  local out st err
  out=$(run "$@" 2>fixtures/.stderr); st=$?
  err=$(<fixtures/.stderr)
  assert_eq "$label: exit status" 2 "$st"
  assert_eq "$label: stderr" "$expected" "$err"
  assert_eq "$label: stdout stays empty" '' "$out"
}

usage='usage: lifegame.sh <pattern-file> <generations>'
err_case 'no arguments' "$usage"
err_case 'one argument' "$usage" fixtures/blinker.txt
err_case 'three arguments' "$usage" fixtures/blinker.txt 2 extra
err_case 'unreadable pattern file' 'error: cannot read fixtures/nope.txt' fixtures/nope.txt 1
gen_err='error: generations must be a non-negative integer'
err_case 'negative generations' "$gen_err" fixtures/blinker.txt -1
err_case 'non-numeric generations' "$gen_err" fixtures/blinker.txt abc
err_case 'fractional generations' "$gen_err" fixtures/blinker.txt 1.5
err_case 'trailing junk in generations' "$gen_err" fixtures/blinker.txt 2x
err_case 'stray character in a row' 'error: invalid character: x' fixtures/badchar.txt 1
err_case 'ragged rows' 'error: ragged pattern' fixtures/ragged.txt 1
err_case 'no grid rows at all' 'error: empty pattern' fixtures/nothing.txt 1

# ---- summary -----------------------------------------------------------------

printf '%d checks, %d failures\n' "$checks" "$fails"
if (( fails > 0 )); then
  exit 1
fi
