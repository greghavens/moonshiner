out=$(run fixtures/blinker.txt 2); st=$?
assert_eq 'blinker exit status' 0 "$st"
assert_eq 'blinker oscillates with period 2' "$expected" "$out"

out=$(run fixtures/commented.txt 2)
assert_eq 'comment and blank lines are ignored by the parser' "$expected" "$out"

