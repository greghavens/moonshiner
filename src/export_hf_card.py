"""Render the Hugging Face dataset card from the single house template.

Every dataset moonshiner generates gets this template. The card is never
hand-written: this program renders it from ``config.json``, the seed corpus,
and the ACTUAL rows being published — so the mix (languages, task categories,
coding vs security), the teacher/judge configuration, the tool surface, split
sizes, and the model attestation rate always follow the data.

Two stages share the one template:

- ``release`` (the ``card`` pipeline phase): renders from the validated
  next-step export in ``data/hf-publish/traces.jsonl``.
- ``preview`` (used by ``publish_preview``): renders while generation is still
  running, from the verified, attested whole-trajectory rows published so far.
  Sections that only exist after screening state that plainly instead of
  carrying fabricated numbers.

Style mirrors the sibling house cards — rich YAML front-matter, a regenerable
``screened-snapshot`` status block, and provenance + limitations sections that
document the verification and independent-judge screening effort behind the
corpus. Every card opens with the moonshiner attribution link. This phase
calls no model; it is safe to run offline.
"""
