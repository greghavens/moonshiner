def _load_rows(path: Path) -> list[dict]:
    if not path.exists():
        raise SystemExit(
            f"no published export at {path} — run `export-next` first")
    # Stream the large cumulative export so rendering does not also hold a
    # second full-file string and split-line copy in memory.
    with path.open() as handle:
        rows = [json.loads(line) for line in handle if line.strip()]
    if not rows:
        raise SystemExit(f"published export {path} is empty")
    return rows


def _size_category(n: int) -> str:
    if n < 1_000:
        return "n<1K"
    if n < 10_000:
        return "1K<n<10K"
    if n < 100_000:
        return "10K<n<100K"
    if n < 1_000_000:
        return "100K<n<1M"
    return "1M<n<10M"


def _pct(part: int, whole: int) -> int:
    return round(100 * part / whole) if whole else 0


def _pct1(part: int, whole: int) -> str:
    return f"{100 * part / whole:.1f}%" if whole else "0.0%"


def _human_size(size: int) -> str:
    """Match Hugging Face's decimal dataset-size convention."""
    if size < 1_000_000:
        return f"{size / 1_000:.0f} kB"
    if size < 1_000_000_000:
        return f"{int(size / 1_000_000)} MB"
    return f"{size / 1_000_000_000:.1f} GB"


def _display_model(model_id: str) -> str:
    from model_profile import display_name
    return display_name(model_id)


def _capability_seed_ids() -> set[str]:
    """Identify instruction-following/tool-use trajectories in legacy rows."""
    ids: set[str] = set()
    for path in CAPABILITY_SEEDS_DIR.glob("*.json"):
        try:
            seed_id = json.loads(path.read_text()).get("id")
        except (OSError, json.JSONDecodeError):
            continue
        if isinstance(seed_id, str):
            ids.add(seed_id)
    return ids


def _display_language(value: str | None) -> str:
    aliases = {
        "non-code": "English", "seed-authoring": "English", "en": "English",
        "py": "Python", "python": "Python", "ts": "TypeScript",
        "typescript": "TypeScript", "js": "JavaScript", "javascript": "JavaScript",
        "go": "Go", "rust": "Rust", "java": "Java", "ruby": "Ruby",
        "csharp": "C#", "cpp": "C++", "c": "C", "bash": "Bash",
        "zsh": "Zsh", "powershell": "PowerShell", "asm": "Assembly",
    }
    raw = str(value or "unknown")
    return aliases.get(raw.casefold(), raw)


def _trajectories(rows: list[dict]) -> dict[str, dict]:
    """Collapse rows to one entry per source trajectory (release: prefix rows
    share a source_trajectory_id; preview: each row IS one trajectory)."""
    view: dict[str, dict] = {}
    capability_ids = _capability_seed_ids()
    for row in rows:
        tid = row.get("source_trajectory_id") or row.get("task")
        domain = row.get("domain")
        if row.get("task") in capability_ids:
            domain = "instruction-following/tool-use"
        language = _display_language(row.get("lang"))
        if row.get("task") in capability_ids:
            language = "English"
        entry = view.setdefault(tid, {
            "task": row.get("task"),
            "lang": language,
            "category": row.get("category") or "unknown",
            "domain": domain or "coding",
            "split": row.get("split"),
            "steps": row.get("assistant_steps") or 0,
            "tools_used": set(),
        })
        entry["tools_used"].update(row.get("tools_used") or [])
        if not entry["steps"] and row.get("messages"):
            entry["steps"] = sum(1 for m in row["messages"]
                                 if m.get("role") == "assistant")
        if not entry["tools_used"] and row.get("messages"):
            entry["tools_used"].update(
                call.get("function", {}).get("name")
                for m in row["messages"]
                for call in (m.get("tool_calls") or [])
                if call.get("function", {}).get("name"))
    return view


def _offered_tools(rows: list[dict]) -> list[str]:
    for row in rows:
        try:
            schemas = json.loads(row.get("tools") or "[]")
        except json.JSONDecodeError:
            continue
        names = [s.get("function", {}).get("name") for s in schemas
                 if isinstance(s, dict)]
        names = [n for n in names if n]
        if names:
            return names
    return list(OFFERED_TOOLS)


def _mix_table(counter: Counter, total: int, head: str) -> str:
    lines = [f"| {head} | Trajectories | Share |", "| --- | ---: | ---: |"]
    for key, count in counter.most_common():
        if not count:
            continue
        lines.append(f"| `{key}` | {count:,} | {_pct1(count, total)} |")
    return "\n".join(lines)


def _program_assignments() -> dict[str, str]:
    """Load catalog assignments, supplementing the source catalog with the
    installed corpus catalog so resumed traces retain their original labels."""
    by_id: dict[str, str] = {}
    paths = (DATA.parent / "corpora" / "active" / "SEED_CATALOG.json",
             ROOT / "SEED_CATALOG.json")
    for path in paths:
        try:
            catalog = json.loads(path.read_text())
        except (OSError, json.JSONDecodeError):
            continue
        by_id.update({item.get("id"): item.get("program")
                      for items in (catalog.get("categories") or {}).values()
                      for item in items
                      if isinstance(item, dict) and item.get("id") and item.get("program")})
    return by_id


def _published_program(task: str | None, category: str | None,
                       by_id: dict[str, str]) -> str:
    if task in by_id:
        return by_id[task]
    if category and category != "unknown":
        return str(category).replace("-", " ").capitalize()
    return "Uncategorized"


def _program_mix(trajectories: dict[str, dict]) -> Counter:
    """Use catalog programs, falling back to explicit published categories."""
    by_id = _program_assignments()
    result: Counter = Counter()
    for entry in trajectories.values():
        result[_published_program(entry.get("task"), entry.get("category"), by_id)] += 1
    return result


def _program_row_mix(rows: list[dict]) -> Counter:
    """Count training rows using the catalog's explicit program assignment."""
    by_id = _program_assignments()
    return Counter(_published_program(row.get("task"), row.get("category"), by_id)
                   for row in rows)


def _program_table(counter: Counter, total: int, row_counter: Counter,
                   total_rows: int) -> str:
    try:
        programs = json.loads((ROOT / "SEED_CATALOG.json").read_text()).get("programs") or {}
    except (OSError, json.JSONDecodeError):
        programs = {}
    lines = ["| kind | trajectories | share | row share | flavor |",
             "|---|---:|---:|---:|---|"]
    for name, count in counter.most_common():
        if count:
            lines.append(
                f"| {name} | {count:,} | {_pct1(count, total)} | "
                f"{_pct1(row_counter.get(name, 0), total_rows)} | "
                f"{programs.get(name, {}).get('description', 'Accepted trajectories grouped by their explicit published dataset category.')} |")
    return "\n".join(lines)


def _front_matter(pretty_name: str, license_id: str, tags: list[str],
                  size_cat: str, has_security: bool) -> str:
    task_categories = ["text-generation"]
    if has_security:
        task_categories.append("question-answering")
    lines = ["---", f"pretty_name: {pretty_name}", f"license: {license_id}",
             "language:", "  - en", "annotations_creators:",
             "  - machine-generated", "task_categories:"]
    lines += [f"  - {cat}" for cat in task_categories]
    lines += ["size_categories:", f"  - {size_cat}", "tags:"]
    # JSON string quoting is valid YAML and prevents numeric-looking model
    # components (for example `5`) from becoming non-string tag values.
    lines += [f"  - {json.dumps(str(tag))}" for tag in tags]
    lines.append("---")
    return "\n".join(lines)


